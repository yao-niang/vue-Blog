<template>
  <form
    id="data-form-container"
    class="data-form-container"
    @submit.prevent="handleSubmit"
  >
    <div class="form-item">
      <div class="input-area">
        <input
          type="text"
          maxlength="10"
          placeholder="用户昵称"
          v-model="fromData.nickname"
        />
        <span class="tip">{{ fromData.nickname.length }}/10</span>
      </div>
      <div class="error">{{ error.nickname }}</div>
    </div>
    <div class="form-item">
      <div class="text-area">
        <textarea
          maxlength="300"
          placeholder="输入内容"
          v-model="fromData.content"
        ></textarea>
        <span class="tip">{{ fromData.content.length }}/300</span>
      </div>
      <div class="error">{{ error.content }}</div>
    </div>
    <div class="form-item">
      <div class="button-area">
        <button :disabled="isSubmiting">
          {{ isSubmiting ? "提交中..." : "提交" }}
        </button>
      </div>
    </div>
  </form>
</template>

<script>
export default {
  data() {
    return {
      fromData: {
        nickname: "",
        content: "",
      },
      error: {
        nickname: "",
        content: "",
      },
      isSubmiting: false,
    };
  },
  methods: {
    handleSubmit() {
      this.error.nickname = this.fromData.nickname ? "" : "不知道要写昵称吗？";
      this.error.content = this.fromData.content ? "" : "不知道要写评论的内容吗";
      if (this.error.nickname || this.error.content) {
        return;
      }
      this.isSubmiting = true;
      this.$emit("submit22", this.fromData, (successMes) => {
        this.$showMessage({
          content: successMes,
          type: "error",
          duration: 1000,
          container: this.$refs.from,
          callback: () => {
            this.fromData.nickname = "";
            this.fromData.content = "";
            this.isSubmiting = false;
          },
        });
      });
    },
  },
};
</script>

